class Address:
    def __init__(self, country:str, state:str, city:str, street:str, postal:int):
        self.country = country
        self.state = state
        self.city = city
        self.street = street
        self.postal = postal

        def get_address(self):
            self.get_address
            return f"{street}, {city}, {state}, {postal}, {country}"